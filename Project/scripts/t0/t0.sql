LIST @YELLOW_RAW_STAGE;
LIST @GREEN_RAW_STAGE;
LIST @FHV_RAW_STAGE;
LIST @FHVHV_RAW_STAGE;

CREATE OR REPLACE TABLE T0_SCHEMA_ALL AS
SELECT * FROM T0_YELLOW_SCHEMA
UNION ALL
SELECT * FROM T0_GREEN_SCHEMA
UNION ALL
SELECT * FROM T0_FHV_SCHEMA
UNION ALL
SELECT * FROM T0_FHVHV_SCHEMA;

SELECT
  dataset,
  COUNT(*) AS inferred_columns
FROM T0_SCHEMA_ALL
GROUP BY dataset
ORDER BY dataset;