USE WAREHOUSE BIGDATA_MZMB_WH;
USE DATABASE BIGDATA_TAXI_MZMB;
USE SCHEMA BRONZE;

CREATE OR REPLACE FILE FORMAT PARQUET_FF
  TYPE = PARQUET;

CREATE OR REPLACE STAGE YELLOW_RAW_STAGE
  FILE_FORMAT = PARQUET_FF;

CREATE OR REPLACE STAGE GREEN_RAW_STAGE
  FILE_FORMAT = PARQUET_FF;

CREATE OR REPLACE STAGE FHV_RAW_STAGE
  FILE_FORMAT = PARQUET_FF;

CREATE OR REPLACE STAGE FHVHV_RAW_STAGE
  FILE_FORMAT = PARQUET_FF;

LIST @YELLOW_RAW_STAGE;

CREATE OR REPLACE TABLE T0_YELLOW_SCHEMA AS
SELECT
  'yellow' AS dataset,
  column_name,
  type AS column_type,
  nullable,
  expression,
  filenames,
  order_id
FROM TABLE(
  INFER_SCHEMA(
    LOCATION => '@YELLOW_RAW_STAGE',
    FILE_FORMAT => 'PARQUET_FF'
  )
);

LIST @GREEN_RAW_STAGE;

CREATE OR REPLACE TABLE T0_GREEN_SCHEMA AS
